
import processing.core.*;
import java.util.*;
public class Location {
	private int x;
	private int y;
	Random rand = new Random();
	

	
	public Location(){
		int x=0;
		int y=0;
	}
		
	public int getX(){
		return x;
	}
	
	public void setX(int a){
		x=a;
	}
	
	public void setY(int a){
		y=a;
	}
	
	public int getY(){
		return y;
	}
	

	

}
